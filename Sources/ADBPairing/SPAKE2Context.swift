// MARK: - SPAKE2 context

public final class SPAKE2Context {
    public static let messageSize = 32
    public static let keySize     = 64

    private let role:      SPAKE2Role
    private let myName:    [UInt8]
    private let theirName: [UInt8]

    private var privateKey:     [UInt8] = [UInt8](repeating: 0, count: 32)
    private var myMsg:          [UInt8] = [UInt8](repeating: 0, count: 32)
    private var passwordScalar: [UInt8] = [UInt8](repeating: 0, count: 32)
    private var passwordHash:   [UInt8] = [UInt8](repeating: 0, count: 64)

    private enum State { case initial, messageGenerated, keyGenerated }
    private var state = State.initial

    // l = 2^252 + 27742317777372353535851937790883648493
    private static let l: [UInt8] = [
        0xed, 0xd3, 0xf5, 0x5c, 0x1a, 0x63, 0x12, 0x58,
        0xd6, 0x9c, 0xf7, 0xa2, 0xde, 0xf9, 0xde, 0x14,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10
    ]

    public init(role: SPAKE2Role, myName: [UInt8], theirName: [UInt8]) {
        self.role      = role
        self.myName    = myName
        self.theirName = theirName
    }

    public convenience init(role: SPAKE2Role, myName: String, theirName: String) {
        self.init(role: role,
                  myName: Array(myName.utf8),
                  theirName: Array(theirName.utf8))
    }

    // MARK: - generateMessage

    /// Generate the 32-byte message to send to the peer.
    /// - Parameter password: The shared password.
    /// - Returns: 32-byte SPAKE2 message.
    public func generateMessage(password: [UInt8]) throws -> [UInt8] {
        var randomKey = [UInt8](repeating: 0, count: 64)
        for i in 0..<64 { randomKey[i] = UInt8.random(in: 0...255) }
        return try generateMessage(password: password, privateKeyMaterial: randomKey)
    }

    // Internal: allow deterministic private key for testing.
    func generateMessage(password: [UInt8], privateKeyMaterial: [UInt8]) throws -> [UInt8] {
        guard state == .initial else { throw SPAKE2Error.alreadyGeneratedMessage }
        precondition(privateKeyMaterial.count == 64)

        // Reduce 64-byte key material to scalar
        var privKey = privateKeyMaterial
        Scalar25519.reduce(&privKey)
        // Multiply by cofactor 8
        Scalar25519.leftShift3(&privKey)
        privateKey = Array(privKey[0..<32])

        // P = privateKey * G
        let P = G.scalarMultiply(privateKey)

        // Hash password with SHA-512
        var pwHash = [UInt8](SHA512.hash(data: password))
        passwordHash = pwHash

        // Reduce password hash to scalar
        Scalar25519.reduce(&pwHash)

        // SPAKE2 cofactor-clearing hack (match BoringSSL behaviour):
        // If the password scalar has low bits set, add multiples of l to clear them.
        var pwScalar = pwHash
        applyPasswordScalarHack(&pwScalar)
        passwordScalar = pwScalar

        // mask = passwordScalar * (Alice→M, Bob→N)
        let maskTable = role == .alice ? PRECOMP_M : PRECOMP_N
        let mask = geScalarMultiplySmallPrecomp(scalar: passwordScalar, table: maskTable)

        // P* = P + mask
        let PStar = P.toP3().add(mask.toCached()).toP3()

        myMsg = PStar.toBytes()
        state = .messageGenerated
        return myMsg
    }

    // MARK: - processMessage

    /// Process the peer's 32-byte message and return the 64-byte shared key.
    public func processMessage(_ theirMsg: [UInt8]) throws -> [UInt8] {
        guard state == .messageGenerated else { throw SPAKE2Error.messageNotYetGenerated }
        guard theirMsg.count == 32 else {
            throw SPAKE2Error.invalidPeerMessage("message must be 32 bytes, got \(theirMsg.count)")
        }

        // Decode peer's point
        guard let QStar = GroupElement.fromBytesNegateVarTime(theirMsg) else {
            throw SPAKE2Error.pointNotOnCurve
        }

        // peers mask = passwordScalar * (Alice→N, Bob→M)
        let peerMaskTable = role == .alice ? PRECOMP_N : PRECOMP_M
        let peersMask = geScalarMultiplySmallPrecomp(scalar: passwordScalar, table: peerMaskTable)

        // Q = Q* - peersMask
        let Q = QStar.toP3().sub(peersMask.toCached()).toP3()

        // DH = privateKey * Q
        let dhShared = Q.scalarMultiply(privateKey).toBytes()

        // SHA-512( len(A)||A || len(B)||B || msg_A || msg_B || DH || pw_hash )
        var sha = SHA512()
        if role == .alice {
            updateWithLengthPrefix(&sha, myName)
            updateWithLengthPrefix(&sha, theirName)
            updateWithLengthPrefix(&sha, myMsg)
            updateWithLengthPrefix(&sha, theirMsg)
        } else {
            updateWithLengthPrefix(&sha, theirName)
            updateWithLengthPrefix(&sha, myName)
            updateWithLengthPrefix(&sha, theirMsg)
            updateWithLengthPrefix(&sha, myMsg)
        }
        updateWithLengthPrefix(&sha, dhShared)
        updateWithLengthPrefix(&sha, passwordHash)

        let key = [UInt8](sha.finalize())
        state = .keyGenerated
        return key
    }

    // MARK: - Private helpers

    /// SPAKE2 BoringSSL cofactor-clearing password scalar hack.
    private func applyPasswordScalarHack(_ scalar: inout [UInt8]) {
        var order = Self.l
        var tmp = [UInt8](repeating: 0, count: 32)

        // Bit 0
        let mask0 = isEqual(Int(scalar[0]) & 1, 1)
        for i in 0..<32 { tmp[i] = UInt8(truncatingIfNeeded: Int64(order[i]) & mask0) }
        addScalars(&scalar, tmp)
        doubleScalar(&order)

        // Bit 1
        var tmp2 = [UInt8](repeating: 0, count: 32)
        let mask2 = isEqual(Int(scalar[0]) & 2, 2)
        for i in 0..<32 { tmp2[i] = UInt8(truncatingIfNeeded: Int64(order[i]) & mask2) }
        addScalars(&scalar, tmp2)
        doubleScalar(&order)

        // Bit 2
        var tmp3 = [UInt8](repeating: 0, count: 32)
        let mask4 = isEqual(Int(scalar[0]) & 4, 4)
        for i in 0..<32 { tmp3[i] = UInt8(truncatingIfNeeded: Int64(order[i]) & mask4) }
        addScalars(&scalar, tmp3)
    }

    private func isEqual(_ a: Int, _ b: Int) -> Int64 {
        // 0xFFFF...FF if equal, 0 otherwise
        let x = UInt64(bitPattern: Int64(a ^ b))
        let z = (~x & (x &- 1)) >> 63
        return -Int64(bitPattern: z)
    }

    private func addScalars(_ a: inout [UInt8], _ b: [UInt8]) {
        var carry = 0
        for i in 0..<32 {
            let sum = Int(a[i]) + Int(b[i]) + carry
            a[i] = UInt8(sum & 0xff)
            carry = sum >> 8
        }
    }

    private func doubleScalar(_ s: inout [UInt8]) {
        var carry = 0
        for i in 0..<32 {
            let out = (Int(s[i]) << 1) | carry
            carry = (Int(s[i]) >> 7) & 1
            s[i] = UInt8(out & 0xff)
        }
    }

    /// SHA-512 length-prefixed update (little-endian 8-byte length).
    private func updateWithLengthPrefix(_ sha: inout SHA512, _ data: [UInt8]) {
        var len = UInt64(data.count)
        var lenBytes = [UInt8](repeating: 0, count: 8)
        for i in 0..<8 { lenBytes[i] = UInt8(len & 0xff); len >>= 8 }
        sha.update(data: lenBytes)
        sha.update(data: data)
    }

    /// Multiply scalar `a` by the precomp table (4-bit windowed double-and-add, 64 iterations).
    /// Matches Java Spake2Context.geScalarMultiplySmallPrecomp.
    private func geScalarMultiplySmallPrecomp(scalar a: [UInt8], table: [GroupElement]) -> GroupElement {
        // PRECOMP identity for point (x=0, y=1): (y+x, y-x, 2dxy) = (1, 1, 0).
        // Must match Java Curve.zeroPrecomp = precomp(one, one, zero).
        let zero = GroupElement(precomp: .one, ymx: .one, xy2d: .zero)
        var h = GroupElement.zero
        for i in stride(from: 63, through: 0, by: -1) {
            var index = 0
            for j in 0..<4 {
                let byteIdx = 8 * j + (i >> 3)
                let bit = Int((a[byteIdx] >> (i & 7)) & 1)
                index |= (bit << j)
            }
            var e = zero
            for j in 1..<16 {
                e = e.cmov(table[j - 1], flag: Utils25519.equal(index, j))
            }
            h = h.dbl().toP3().madd(e).toP3()
        }
        return h
    }
}

// MARK: - Constant-time utilities

enum Utils25519 {
    static func equal(_ a: Int, _ b: Int) -> Int {
        // Returns 1 if a == b, 0 otherwise (constant-time)
        var x = UInt32(bitPattern: Int32(a ^ b))
        x |= x >> 16
        x |= x >> 8
        x |= x >> 4
        x |= x >> 2
        x |= x >> 1
        return Int(~x & 1)
    }
}
