// SPAKE2Context.swift
// ADBPairing sub-package — SwiftADB
//
// Swift port of io.github.muntashirakon.crypto.spake2.Spake2Context
//
// SPAKE2 over Ed25519 using the SAME constants and protocol as BoringSSL / Android ADB.
//
// Protocol summary:
//   Alice (client): computes P* = x·G + h(pw)·M, sends P*
//   Bob   (server): computes Q* = y·G + h(pw)·N, sends Q*
//   Both sides: recover DH = x·(Q* - h(pw)·N)  [or symmetrically for Bob]
//   Both sides: derive shared key via SHA-512 over names + messages + DH + pw-hash

import CryptoKit
import Foundation

// MARK: - Roles

public enum SPAKE2Role {
    case alice  // ADB client
    case bob    // ADB server / phone
}

// MARK: - Errors

public enum SPAKE2Error: Error, LocalizedError {
    case alreadyGeneratedMessage
    case alreadyGeneratedKey
    case messageNotYetGenerated
    case invalidPeerMessage(String)
    case pointNotOnCurve

    public var errorDescription: String? {
        switch self {
        case .alreadyGeneratedMessage:  return "SPAKE2: generateMessage already called"
        case .alreadyGeneratedKey:      return "SPAKE2: processMessage already called"
        case .messageNotYetGenerated:   return "SPAKE2: call generateMessage first"
        case .invalidPeerMessage(let m): return "SPAKE2: invalid peer message — \(m)"
        case .pointNotOnCurve:          return "SPAKE2: peer point not on curve"
        }
    }
}

