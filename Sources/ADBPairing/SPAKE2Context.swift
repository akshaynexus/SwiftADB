// MARK: - Table parsing

private func geFromTable(_ raw: [UInt8]) -> [GroupElement] {
    let n = raw.count / 96  // 96 = 3 * 32
    var result = [GroupElement]()
    result.reserveCapacity(n)
    for i in 0..<n {
        let base = i * 96
        let ypx  = Ed25519FieldElement.fromBytes(Array(raw[base..<base+32]))
        let ymx  = Ed25519FieldElement.fromBytes(Array(raw[base+32..<base+64]))
        let xy2d = Ed25519FieldElement.fromBytes(Array(raw[base+64..<base+96]))
        result.append(GroupElement(precomp: ypx, ymx: ymx, xy2d: xy2d))
    }
    return result
}

